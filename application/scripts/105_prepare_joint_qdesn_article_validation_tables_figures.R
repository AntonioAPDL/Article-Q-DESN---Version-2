latex_escape <- function(x) {
  x <- as.character(x)
  x <- gsub("\\\\", "\\\\textbackslash{}", x)
  x <- gsub("([#$%&_{}])", "\\\\\\1", x, perl = TRUE)
  x <- gsub("~", "\\\\textasciitilde{}", x, fixed = TRUE)
  x <- gsub("\\^", "\\\\textasciicircum{}", x)
  x
}

fmt_num <- function(x, digits = 3) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  if (abs(x) >= 1000) return(formatC(x, format = "f", digits = 1, big.mark = ","))
  if (abs(x) >= 100) return(formatC(x, format = "f", digits = 1))
  formatC(x, format = "f", digits = digits)
}

fmt_int <- function(x) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  formatC(round(x), format = "d", big.mark = ",")
}

fmt_pair <- function(mae, rmse) sprintf("%s (%s)", fmt_num(mae, 3), fmt_num(rmse, 3))

article_model_label <- function(x) {
  x <- as.character(x)
  out <- ifelse(x == "JOINT QDESN RHS", "Joint QDESN RHS", x)
  out <- ifelse(out == "QDESN RHS", "Independent QDESN RHS", out)
  out <- ifelse(out == "JOINT exQDESN RHS", "Joint exQDESN RHS", out)
  out <- ifelse(out == "exQDESN RHS", "Independent exQDESN RHS", out)
  out
}

