  cols <- c("#0072B2", "#009E73", "#D55E00", "#CC79A7")[seq_along(mods)]
  pdf_plot(path, width = 6.9, height = 4.7, {
    graphics::par(mar = c(4.7, 4.7, 2.8, 1.1))
    yr <- range(agg$truth_mae, finite = TRUE)
    graphics::plot(range(agg$tau), yr * c(0.90, 1.10), type = "n", xlab = "Target quantile level", ylab = "Forecast MAE to true quantile", main = "Forecast truth error by quantile")
    graphics::grid(col = "grey88")
    for (kk in seq_along(mods)) {
      block <- agg[agg$Model == mods[[kk]], , drop = FALSE]
