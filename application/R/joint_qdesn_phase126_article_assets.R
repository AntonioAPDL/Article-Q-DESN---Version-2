  n_scenarios <- length(unique(src$scope$scenario_id))
  n_models <- length(unique(src$scope$source_model_id))
  tau_grid <- unique(src$case$tau_grid %||% NA_character_)
  tau_grid <- tau_grid[!is.na(tau_grid)]
  data.frame(
    Item = c(
      "Evidence layer",
      "Synthetic scenarios",
      "Model rows",
      "Quantile grid",
      "Fit window",
      "Forecast window",
      "Prediction contract",
      "Diagnostic policy"
    ),
    Value = c(
      "VB/VB-LD screening and calibration followed by VB-initialized MCMC confirmation.",
      sprintf("%d mechanisms: three bridge cases and five stress cases with known conditional quantile paths.", n_scenarios),
      sprintf("%d readout-likelihood combinations: Joint QDESN RHS, Independent QDESN RHS, Joint exQDESN RHS, and Independent exQDESN RHS.", n_models),
      if (length(tau_grid)) gsub(",", ", ", tau_grid[[1L]], fixed = TRUE) else "0.05, 0.10, 0.25, 0.50, 0.75, 0.90, 0.95",
      sprintf("%s observations after DESN washout.", unique(src$case$n_train)[[1L]] %||% 500),
      "No-refit held-out forecast scoring over the common validation origin grid inherited from the frozen validation fixtures.",
      "Scores validate posterior quantile grids/readout paths, not a scalar posterior predictive density.",
      "Reported scores use monotone contract quantile grids; raw adjacent-level crossings are retained as pre-contract diagnostics."
    ),
    stringsAsFactors = FALSE
  )
}

app_joint_qdesn_phase126_readiness <- function(src, asset_manifest) {
  source_gate <- app_joint_qdesn_phase126_source_gate(src)
  model <- src$model
  case <- src$case
  raw_total <- sum(case$mcmc_forecast_raw_crossing_pairs, na.rm = TRUE)
  contract_total <- sum(case$mcmc_forecast_contract_crossing_pairs, na.rm = TRUE)
  data.frame(
    overall_gate = ifelse(identical(source_gate, "pass") && contract_total == 0L, "review", "fail"),
    hard_implementation_gate = source_gate,
    balanced_cells = nrow(src$scope),
    n_scenarios = length(unique(src$scope$scenario_id)),
    n_model_rows = nrow(model),
    n_case_rows = nrow(case),
    n_pass_cases = sum(case$gate_status == "pass", na.rm = TRUE),
    n_review_cases = sum(case$gate_status == "review", na.rm = TRUE),
    n_fail_cases = sum(case$gate_status == "fail", na.rm = TRUE),
    mcmc_forecast_raw_crossing_pairs = raw_total,
    mcmc_forecast_contract_crossing_pairs = contract_total,
    primary_joint_qdesn_raw_crossings = sum(model$mcmc_forecast_raw_crossing_pairs[model$model_label == "Joint QDESN RHS"], na.rm = TRUE),
    independent_qdesn_raw_crossings = sum(model$mcmc_forecast_raw_crossing_pairs[model$model_label == "Independent QDESN RHS"], na.rm = TRUE),
    joint_exqdesn_raw_crossings = sum(model$mcmc_forecast_raw_crossing_pairs[model$model_label == "Joint exQDESN RHS"], na.rm = TRUE),
    independent_exqdesn_raw_crossings = sum(model$mcmc_forecast_raw_crossing_pairs[model$model_label == "Independent exQDESN RHS"], na.rm = TRUE),
    n_article_assets = nrow(asset_manifest),
    recommended_article_message = "Use balanced MCMC confirmation as article-facing evidence; keep raw crossings as diagnostics and contract crossings as scored-grid behavior.",
    stringsAsFactors = FALSE
  )
}

