app_joint_qdesn_phase126_verify_manifest <- function(dir, manifest_name = "artifact_manifest.csv", artifact_label = "artifact") {
  manifest_path <- file.path(dir, manifest_name)
  if (!file.exists(manifest_path)) {
    return(data.frame(
      artifact_label = artifact_label,
      label = manifest_name,
      relative_path = manifest_name,
      path = normalizePath(manifest_path, winslash = "/", mustWork = FALSE),
      exists = FALSE,
      declared_size_bytes = NA_real_,
      actual_size_bytes = NA_real_,
      declared_sha256 = NA_character_,
      actual_sha256 = NA_character_,
      status = "fail",
      stringsAsFactors = FALSE
    ))
  }
  manifest <- app_read_csv(manifest_path)
  app_check_required_columns(manifest, c("label", "relative_path", "size_bytes", "sha256"), artifact_label)
  app_bind_rows_fill(lapply(seq_len(nrow(manifest)), function(ii) {
    rel <- manifest$relative_path[[ii]]
    path <- if (grepl("^/", rel)) rel else file.path(dir, rel)
    exists <- file.exists(path)
    actual_sha <- if (exists) app_sha256_file(path) else NA_character_
    actual_size <- if (exists) as.numeric(file.info(path)$size) else NA_real_
    data.frame(
      artifact_label = artifact_label,
      label = manifest$label[[ii]],
      relative_path = rel,
      path = normalizePath(path, winslash = "/", mustWork = FALSE),
      exists = exists,
      declared_size_bytes = as.numeric(manifest$size_bytes[[ii]]),
      actual_size_bytes = actual_size,
      declared_sha256 = manifest$sha256[[ii]],
      actual_sha256 = actual_sha,
      status = if (exists &&
        identical(as.numeric(actual_size), as.numeric(manifest$size_bytes[[ii]])) &&
        identical(tolower(actual_sha), tolower(manifest$sha256[[ii]]))) "pass" else "fail",
      stringsAsFactors = FALSE
    )
  }))
}

app_joint_qdesn_phase126_verify_article_asset_manifest <- function(manifest_path) {
  if (!file.exists(manifest_path)) {
    return(data.frame(
      label = "article_asset_manifest",
      path = normalizePath(manifest_path, winslash = "/", mustWork = FALSE),
      exists = FALSE,
      status = "fail",
      stringsAsFactors = FALSE
    ))
  }
  manifest <- app_read_csv(manifest_path)
  app_check_required_columns(manifest, c("label", "path", "size_bytes", "sha256"), "Phase126 article asset manifest")
  app_bind_rows_fill(lapply(seq_len(nrow(manifest)), function(ii) {
    path <- manifest$path[[ii]]
    abs_path <- if (grepl("^/", path)) path else app_path(path)
    exists <- file.exists(abs_path)
    actual_sha <- if (exists) app_sha256_file(abs_path) else NA_character_
    actual_size <- if (exists) as.numeric(file.info(abs_path)$size) else NA_real_
    data.frame(
      label = manifest$label[[ii]],
      artifact_type = manifest$artifact_type[[ii]] %||% NA_character_,
      path = normalizePath(abs_path, winslash = "/", mustWork = FALSE),
      exists = exists,
      declared_size_bytes = as.numeric(manifest$size_bytes[[ii]]),
      actual_size_bytes = actual_size,
      declared_sha256 = manifest$sha256[[ii]],
      actual_sha256 = actual_sha,
      status = if (exists &&
        identical(as.numeric(actual_size), as.numeric(manifest$size_bytes[[ii]])) &&
        identical(tolower(actual_sha), tolower(manifest$sha256[[ii]]))) "pass" else "fail",
      stringsAsFactors = FALSE
    )
  }))
}

