app_joint_qdesn_article_label <- function(x) {
  x <- as.character(x)
  out <- ifelse(x == "JOINT QDESN RHS", "Joint QDESN RHS", x)
  out <- ifelse(out == "QDESN RHS", "Independent QDESN RHS", out)
  out <- ifelse(out == "JOINT exQDESN RHS", "Joint exQDESN RHS", out)
  out <- ifelse(out == "exQDESN RHS", "Independent exQDESN RHS", out)
  out <- ifelse(out == "JOINT QDESN RHS MCMC", "Joint QDESN RHS MCMC", out)
  out
}

app_joint_qdesn_article_scenario_label <- function(x) {
  out <- tools::toTitleCase(gsub("_", " ", as.character(x), fixed = TRUE))
  out <- ifelse(out == "Student T Location Scale", "Student-t Location-Scale", out)
  out <- ifelse(out == "Student t Location Scale", "Student-t Location-Scale", out)
  out <- ifelse(out == "Gaussian Mixture Bridge", "Gaussian-Mixture Bridge", out)
  out
}

app_joint_qdesn_article_family_label <- function(x) {
  out <- tools::toTitleCase(gsub("_", " ", as.character(x), fixed = TRUE))
  out <- ifelse(out %in% c("Student T", "Student t"), "Student-t", out)
  out <- ifelse(out == "Gaussian Mixture", "Gaussian mixture", out)
  out
}

app_joint_qdesn_article_fmt_num <- function(x, digits = 3) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  if (abs(x[[1L]]) >= 1000) return(formatC(x[[1L]], format = "f", digits = 1, big.mark = ","))
  formatC(x[[1L]], format = "f", digits = digits)
}

app_joint_qdesn_article_fmt_int <- function(x) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  formatC(round(x[[1L]]), format = "d", big.mark = ",")
}

app_joint_qdesn_article_fmt_pair <- function(mae, rmse) {
  sprintf(
    "%s (%s)",
    app_joint_qdesn_article_fmt_num(mae, 3),
    app_joint_qdesn_article_fmt_num(rmse, 3)
  )
}

app_joint_qdesn_article_readout_label <- function(x) {
  x <- as.character(x)
  out <- ifelse(x == "joint", "joint", x)
  out <- ifelse(out == "independent_single_tau", "independent", out)
  out
}

app_joint_qdesn_article_likelihood_label <- function(x) {
  x <- tolower(as.character(x))
  out <- ifelse(x == "al", "AL", x)
  out <- ifelse(out == "exal", "exAL", out)
  out
}

app_joint_qdesn_article_diag_note <- function(x) {
  x <- as.character(x)
  x <- ifelse(x == "all readiness gates passed", "all diagnostics finite", x)
  x <- gsub("VB reached max iterations", "VB initializer reached the iteration budget", x, fixed = TRUE)
  x <- gsub("raw quantiles crossed before contract step", "pre-rearrangement quantiles crossed", x, fixed = TRUE)
  x <- gsub("large monotone adjustment", "monotone adjustment flagged", x, fixed = TRUE)
  x
}

app_joint_qdesn_article_latex_escape <- function(x) {
  x <- as.character(x)
  x <- gsub("\\\\", "\\\\textbackslash{}", x)
  x <- gsub("([#$%&_{}])", "\\\\\\1", x, perl = TRUE)
  x <- gsub("~", "\\\\textasciitilde{}", x, fixed = TRUE)
  x <- gsub("\\^", "\\\\textasciicircum{}", x)
  x
}

