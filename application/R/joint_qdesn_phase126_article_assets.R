    source_vb_freeze_manifest_verification = vb_manifest,
    fixture_source_manifest_verification = fixture_manifest,
    gate = gate,
    scope = scope,
    model = model,
    case = case,
    winners = winners,
    raw_contract = raw_contract,
    run_config = run_config,
    health = health,
    recommendation = recommendation
  )
}

app_joint_qdesn_phase126_source_gate <- function(src) {
  checks <- c(
    all(src$artifact_manifest_verification$status == "pass"),
    all(src$source_artifact_manifest_verification$status == "pass"),
    all(src$source_vb_freeze_manifest_verification$status == "pass"),
    all(src$fixture_source_manifest_verification$status == "pass"),
    all(src$scope$present_in_balanced_mcmc),
    all(src$scope$n_matching_rows == 1L),
    !any(src$gate$status == "fail"),
    identical(as.character(src$run_config$hard_implementation_gate[[1L]]), "pass")
  )
  if (all(checks)) "pass" else "fail"
}

app_joint_qdesn_phase126_model_order <- function(model_label) {
  match(model_label, c(
    "Joint QDESN RHS",
    "Independent QDESN RHS",
    "Joint exQDESN RHS",
    "Independent exQDESN RHS"
  ))
}

app_joint_qdesn_phase126_make_model_table <- function(model) {
  model <- model[order(app_joint_qdesn_phase126_model_order(model$model_label)), , drop = FALSE]
  data.frame(
    Model = model$model_label,
    Cases = vapply(model$n_cases, app_joint_qdesn_phase126_fmt_int, character(1L)),
    `Fit MAE` = vapply(model$mcmc_fit_truth_mae, app_joint_qdesn_phase126_fmt_num, character(1L), digits = 3),
    `Forecast MAE` = vapply(model$mcmc_forecast_truth_mae, app_joint_qdesn_phase126_fmt_num, character(1L), digits = 3),
    `Check loss` = vapply(model$mcmc_forecast_check_loss, app_joint_qdesn_phase126_fmt_num, character(1L), digits = 3),
    `Grid CRPS` = vapply(model$mcmc_forecast_crps_grid, app_joint_qdesn_phase126_fmt_num, character(1L), digits = 3),
    `Hit error` = vapply(model$mcmc_abs_hit_rate_error, app_joint_qdesn_phase126_fmt_num, character(1L), digits = 3),
    `Raw crossings` = vapply(model$mcmc_forecast_raw_crossing_pairs, app_joint_qdesn_phase126_fmt_int, character(1L)),
    `Contract crossings` = vapply(model$mcmc_forecast_contract_crossing_pairs, app_joint_qdesn_phase126_fmt_int, character(1L)),
    Gate = model$gate_status,
    stringsAsFactors = FALSE,
    check.names = FALSE
  )
}

app_joint_qdesn_phase126_make_scenario_table <- function(case, bold_winners = FALSE) {
  model_labels <- c("Joint QDESN RHS", "Independent QDESN RHS", "Joint exQDESN RHS", "Independent exQDESN RHS")
  rows <- lapply(unique(case$scenario_id), function(sc) {
    block <- case[case$scenario_id == sc & case$model_label %in% model_labels, , drop = FALSE]
    block <- block[order(app_joint_qdesn_phase126_model_order(block$model_label)), , drop = FALSE]
    min_mae <- min(block$mcmc_forecast_truth_mae, na.rm = TRUE)
    vals <- setNames(rep("--", length(model_labels)), model_labels)
    for (ii in seq_len(nrow(block))) {
      val <- sprintf(
        "%s (%s)",
        app_joint_qdesn_phase126_fmt_num(block$mcmc_forecast_truth_mae[[ii]], 3),
        app_joint_qdesn_phase126_fmt_num(block$mcmc_fit_truth_mae[[ii]], 3)
      )
      if (isTRUE(bold_winners) && isTRUE(all.equal(block$mcmc_forecast_truth_mae[[ii]], min_mae, tolerance = 1e-12))) {
        val <- sprintf("\\textbf{%s}", val)
      }
      vals[[block$model_label[[ii]]]] <- val
    }
    data.frame(
      Scenario = block$scenario_label[[1L]],
      `Joint QDESN` = vals[["Joint QDESN RHS"]],
      `Independent QDESN` = vals[["Independent QDESN RHS"]],
      `Joint exQDESN` = vals[["Joint exQDESN RHS"]],
      `Independent exQDESN` = vals[["Independent exQDESN RHS"]],
      stringsAsFactors = FALSE,
      check.names = FALSE
    )
  })
  app_bind_rows_fill(rows)
}

app_joint_qdesn_phase126_make_gate_table <- function(gate) {
  keep <- gate$gate %in% c(
    "source_artifact_manifests", "source_vb_freeze_manifests",
    "fixture_source_manifests", "worker_failures", "balanced_grid_complete",
    "finite_mcmc_draws", "provided_vb_initialization", "contract_crossings",
    "raw_crossings", "chain_stability", "vb_mcmc_distance",
    "quantile_grid_predictive_contract", "article_asset_build_readiness"
  )
  out <- gate[keep, c("gate", "status", "detail"), drop = FALSE]
  out$gate <- gsub("_", " ", out$gate, fixed = TRUE)
  names(out) <- c("Gate", "Status", "Detail")
  out
}

app_joint_qdesn_phase126_make_winner_table <- function(winners) {
  metrics <- c(
    mcmc_fit_truth_mae = "Fit MAE",
    mcmc_forecast_truth_mae = "Forecast MAE",
    mcmc_forecast_check_loss_mean = "Check loss",
    crps_grid_mean = "Grid CRPS"
  )
  rows <- lapply(unique(winners$scenario_id), function(sc) {
    block <- winners[winners$scenario_id == sc & winners$metric %in% names(metrics), , drop = FALSE]
    vals <- setNames(rep("--", length(metrics)), unname(metrics))
    for (ii in seq_len(nrow(block))) {
      vals[[unname(metrics[block$metric[[ii]]])]] <- sprintf(
        "%s (%s)",
        block$best_model_label[[ii]],
        app_joint_qdesn_phase126_fmt_num(block$best_value[[ii]], 3)
      )
    }
    data.frame(
      Scenario = block$scenario_label[[1L]],
      `Fit MAE winner` = vals[["Fit MAE"]],
      `Forecast MAE winner` = vals[["Forecast MAE"]],
      `Check-loss winner` = vals[["Check loss"]],
      `Grid-CRPS winner` = vals[["Grid CRPS"]],
      stringsAsFactors = FALSE,
      check.names = FALSE
    )
  })
  app_bind_rows_fill(rows)
}

