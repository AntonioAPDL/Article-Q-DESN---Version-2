#
# This stage consumes the frozen Phase 125 balanced MCMC audit.  It does not
# rerun VB, VB-LD, MCMC, fixture generation, or validation scoring.

app_joint_qdesn_default_phase126_article_assets_dir <- function() {
  app_path("application/cache/joint_qdesn_phase126_article_assets_20260712")
}

app_joint_qdesn_phase126_write_csv <- function(x, path) {
  app_ensure_dir(dirname(path))
  utils::write.csv(x, path, row.names = FALSE, na = "")
  normalizePath(path, winslash = "/", mustWork = TRUE)
}

app_joint_qdesn_phase126_fmt_num <- function(x, digits = 3L) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  formatC(x[[1L]], format = "f", digits = digits)
}

app_joint_qdesn_phase126_fmt_int <- function(x) {
  x <- suppressWarnings(as.numeric(x))
  if (!length(x) || !is.finite(x[[1L]])) return("--")
  formatC(round(x[[1L]]), format = "d", big.mark = ",")
}

app_joint_qdesn_phase126_latex_escape <- function(x) {
  x <- as.character(x)
  x <- gsub("\\\\", "\\\\textbackslash{}", x)
  x <- gsub("([#$%&_{}])", "\\\\\\1", x, perl = TRUE)
  x <- gsub("~", "\\\\textasciitilde{}", x, fixed = TRUE)
  x <- gsub("\\^", "\\\\textasciicircum{}", x)
  x
}

app_joint_qdesn_phase126_write_latex_table <- function(
  x,
  path,
  caption,
  label,
  align = NULL,
  size = "\\scriptsize",
  resize = FALSE,
  raw_latex_cols = character()
) {
  app_ensure_dir(dirname(path))
  if (is.null(align)) align <- paste0("@{}", paste(rep("l", ncol(x)), collapse = ""), "@{}")
  body <- lapply(seq_len(nrow(x)), function(ii) {
    cells <- unlist(x[ii, , drop = FALSE], use.names = FALSE)
    is_raw <- names(x) %in% raw_latex_cols
    cells[!is_raw] <- app_joint_qdesn_phase126_latex_escape(cells[!is_raw])
    paste(cells, collapse = " & ")
  })
  lines <- c(
    "% Generated by application/scripts/130_build_joint_qdesn_phase126_article_assets.R.",
    "\\begin{table}[!htbp]",
    "\\centering",
    size
  )
  if (isTRUE(resize)) lines <- c(lines, "\\resizebox{\\textwidth}{!}{%")
  lines <- c(
    lines,
    sprintf("\\begin{tabular}{%s}", align),
    "\\toprule",
    paste(app_joint_qdesn_phase126_latex_escape(names(x)), collapse = " & "),
    "\\\\",
    "\\midrule",
    paste0(body, " \\\\"),
    "\\bottomrule",
    "\\end{tabular}"
  )
  if (isTRUE(resize)) lines <- c(lines, "}%")
  lines <- c(
    lines,
    sprintf("\\caption{%s}", caption),
    sprintf("\\label{%s}", label),
    "\\end{table}"
  )
  writeLines(lines, path, useBytes = TRUE)
  normalizePath(path, winslash = "/", mustWork = TRUE)
}

