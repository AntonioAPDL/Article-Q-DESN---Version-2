app_joint_qdesn_article_source_verification <- function(phase107_dir, phase109_dir) {
  phase107_top <- app_joint_qdesn_phase108_manifest_verify(phase107_dir, "selected_vb_source")
  phase109 <- app_joint_qdesn_phase108_manifest_verify(phase109_dir, "mcmc_reference_source")
  nested_path <- file.path(phase107_dir, "candidate_manifest_verification.csv")
  phase107_nested <- if (file.exists(nested_path)) {
    nested <- app_read_csv(nested_path)
    nested$artifact_label <- "selected_vb_nested"
    nested
  } else {
    data.frame(
      artifact_label = "selected_vb_nested",
      label = "candidate_manifest_verification",
      relative_path = "candidate_manifest_verification.csv",
      status = "fail",
      stringsAsFactors = FALSE
    )
  }
  app_bind_rows_fill(list(phase107_top, phase107_nested, phase109))
}

app_joint_qdesn_article_selected_row <- function(selected) {
  app_check_required_columns(selected, c("candidate_id"), "selected VB recommendation")
  if ("selected" %in% names(selected)) {
    out <- selected[isTRUE(as.logical(selected$selected)), , drop = FALSE]
    if (nrow(out)) return(out[1L, , drop = FALSE])
  }
  selected[1L, , drop = FALSE]
}

app_joint_qdesn_article_filter_selected_candidate <- function(x, candidate_id, table_name) {
  if (!"candidate_id" %in% names(x)) return(x)
  out <- x[x$candidate_id == candidate_id, , drop = FALSE]
  if (!nrow(out)) {
    stop(sprintf("%s has no rows for selected candidate '%s'.", table_name, candidate_id), call. = FALSE)
  }
  out
}

app_joint_qdesn_article_stop_if_bad_sources <- function(source_verification) {
  bad <- source_verification[!source_verification$status %in% "pass", , drop = FALSE]
  if (nrow(bad)) {
    stop(sprintf("Source manifest verification failed for %d rows.", nrow(bad)), call. = FALSE)
  }
  invisible(TRUE)
}

app_joint_qdesn_article_load_sources <- function(phase107_dir, phase109_dir) {
  phase107_dir <- normalizePath(phase107_dir, mustWork = TRUE)
  phase109_dir <- normalizePath(phase109_dir, mustWork = TRUE)
  source_verification <- app_joint_qdesn_article_source_verification(phase107_dir, phase109_dir)
  app_joint_qdesn_article_stop_if_bad_sources(source_verification)
  selected <- app_joint_qdesn_article_read_required(phase107_dir, "selected_spec_recommendation.csv")
  selected <- app_joint_qdesn_article_selected_row(selected)
  selected_candidate_id <- selected$candidate_id[[1L]]
  health <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "screening_health_summary.csv"),
    selected_candidate_id,
    "selected VB health summary"
  )
  candidate_registry <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "candidate_registry.csv"),
    selected_candidate_id,
    "selected VB candidate registry"
  )
  fit_model <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "fit_model_metric_summary.csv"),
    selected_candidate_id,
    "selected VB fit model summary"
  )
  forecast_model <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "forecast_model_metric_summary.csv"),
    selected_candidate_id,
    "selected VB forecast model summary"
  )
  fit_scenario <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "fit_scenario_metric_summary.csv"),
    selected_candidate_id,
    "selected VB fit scenario summary"
  )
  forecast_scenario <- app_joint_qdesn_article_filter_selected_candidate(
    app_joint_qdesn_article_read_required(phase107_dir, "forecast_scenario_metric_summary.csv"),
    selected_candidate_id,
    "selected VB forecast scenario summary"
  )
  forecast_tau <- app_read_csv(file.path(phase107_dir, "forecast_tau_metric_summary.csv"), required = FALSE)
  if (!is.null(forecast_tau)) {
    forecast_tau <- app_joint_qdesn_article_filter_selected_candidate(
      forecast_tau,
      selected_candidate_id,
      "selected VB forecast tau summary"
    )
  }
  list(
    phase107_dir = phase107_dir,
    phase109_dir = phase109_dir,
    source_verification = source_verification,
    phase107 = list(
      selected = selected,
      health = health,
      candidate_registry = candidate_registry,
      fit_model = fit_model,
      forecast_model = forecast_model,
      fit_scenario = fit_scenario,
      forecast_scenario = forecast_scenario,
      forecast_tau = forecast_tau
    ),
    phase109 = list(
      run_config = app_joint_qdesn_article_read_required(phase109_dir, "run_config.csv"),
      assessment = app_joint_qdesn_article_read_required(phase109_dir, "mcmc_readiness_assessment.csv"),
      summary = app_joint_qdesn_article_read_required(phase109_dir, "mcmc_readiness_summary.csv"),
      truth = app_joint_qdesn_article_read_required(phase109_dir, "truth_distance_summary.csv"),
      check = app_joint_qdesn_article_read_required(phase109_dir, "check_loss_summary.csv"),
      crps = app_joint_qdesn_article_read_required(phase109_dir, "crps_grid_summary.csv"),
      hit = app_joint_qdesn_article_read_required(phase109_dir, "hit_rate_summary.csv"),
      interval = app_joint_qdesn_article_read_required(phase109_dir, "interval_summary.csv"),
      vb_mcmc_distance = app_joint_qdesn_article_read_required(phase109_dir, "vb_mcmc_distance_summary.csv"),
      chain_distance = app_joint_qdesn_article_read_required(phase109_dir, "chain_to_pooled_distance_summary.csv"),
      failures = app_joint_qdesn_article_read_required(phase109_dir, "scenario_worker_failures.csv"),
      fit_truth = app_joint_qdesn_article_read_required(phase109_dir, "fit_truth_comparison.csv")
    )
  )
}

app_joint_qdesn_article_gate <- function(status) {
  status <- as.character(status)
  if (any(status == "fail")) return("fail")
  if (any(status == "review")) return("review")
  "pass"
}

app_joint_qdesn_article_make_vb_model_summary <- function(src) {
  fit <- src$phase107$fit_model
  forecast <- src$phase107$forecast_model
  by <- c("model_id", "display_label", "likelihood", "fit_structure")
  fit_sel <- fit[, c(by, "truth_mae", "truth_rmse", "check_loss_mean", "raw_crossing_pairs", "contract_crossing_pairs", "reached_max_iter", "gate_status"), drop = FALSE]
  names(fit_sel)[names(fit_sel) %in% c("truth_mae", "truth_rmse", "check_loss_mean", "raw_crossing_pairs", "contract_crossing_pairs", "reached_max_iter", "gate_status")] <-
    c("fit_truth_mae", "fit_truth_rmse", "fit_check_loss", "fit_raw_crossings", "fit_contract_crossings", "fit_max_iter_rows", "fit_gate_status")
  fc_sel <- forecast[, c(by, "truth_mae", "truth_rmse", "check_loss_mean", "crps_grid_mean", "abs_hit_rate_error", "abs_coverage_error", "raw_crossing_pairs", "contract_crossing_pairs", "reached_max_iter", "max_abs_adjustment", "gate_status"), drop = FALSE]
  names(fc_sel)[names(fc_sel) %in% c("truth_mae", "truth_rmse", "check_loss_mean", "raw_crossing_pairs", "contract_crossing_pairs", "reached_max_iter", "max_abs_adjustment", "gate_status")] <-
    c("forecast_truth_mae", "forecast_truth_rmse", "forecast_check_loss", "forecast_raw_crossings", "forecast_contract_crossings", "forecast_max_iter_rows", "forecast_max_adjustment", "forecast_gate_status")
  out <- merge(fit_sel, fc_sel, by = by, all = TRUE)
  out$model_label <- app_joint_qdesn_article_label(out$display_label)
  out$model_role <- ifelse(out$fit_structure == "joint" & out$likelihood == "al", "primary joint AL readout",
    ifelse(out$fit_structure == "joint" & out$likelihood == "exal", "joint exAL comparator",
      ifelse(out$fit_structure == "independent_single_tau" & out$likelihood == "al", "independent AL comparator", "independent exAL comparator")))
  out$article_gate <- mapply(function(a, b) {
    vals <- unique(unlist(strsplit(paste(a, b, sep = ";"), ";", fixed = TRUE)))
    app_joint_qdesn_article_gate(trimws(vals[nzchar(vals)]))
  }, out$fit_gate_status, out$forecast_gate_status)
  ord <- match(out$model_id, c(
    "joint_qdesn_rhs_vb", "qdesn_rhs_independent_vb",
    "joint_exqdesn_rhs_vb", "exqdesn_rhs_independent_vb"
  ))
  out[order(ord), , drop = FALSE]
}

