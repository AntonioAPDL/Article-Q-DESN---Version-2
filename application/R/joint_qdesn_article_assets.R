app_joint_qdesn_article_plot_overlay <- function(path, fit_truth, scenario_id, model_id = "joint_qdesn_rhs_mcmc") {
  dat <- fit_truth[fit_truth$scenario_id == scenario_id & fit_truth$model_id == model_id, , drop = FALSE]
  if (!nrow(dat)) return(NA_character_)
  keep_tau <- intersect(c(0.05, 0.50, 0.95), sort(unique(dat$tau)))
  dat <- dat[dat$tau %in% keep_tau, , drop = FALSE]
  agg <- aggregate(cbind(y, true_quantile, qhat) ~ effective_index + tau, dat, mean, na.rm = TRUE)
  obs <- aggregate(y ~ effective_index, agg, mean, na.rm = TRUE)
  taus <- sort(unique(agg$tau))
  cols <- c("#0072B2", "#000000", "#D55E00")[seq_along(taus)]
  app_joint_qdesn_article_pdf_plot(path, width = 7.3, height = 4.8, {
    graphics::par(mar = c(4.6, 4.8, 2.9, 1.0))
    yr <- range(c(obs$y, agg$true_quantile, agg$qhat), finite = TRUE)
    graphics::plot(obs$effective_index, obs$y, pch = 16, cex = 0.28,
      col = grDevices::adjustcolor("grey35", 0.45), ylim = yr,
      xlab = "Effective time index", ylab = "Observed value and quantile",
      main = app_joint_qdesn_article_scenario_label(scenario_id))
    graphics::grid(col = "grey88")
    for (kk in seq_along(taus)) {
      block <- agg[abs(agg$tau - taus[[kk]]) < 1e-12, , drop = FALSE]
      block <- block[order(block$effective_index), , drop = FALSE]
      graphics::lines(block$effective_index, block$true_quantile, col = cols[[kk]], lwd = 2.0, lty = 2)
      graphics::lines(block$effective_index, block$qhat, col = cols[[kk]], lwd = 1.6)
    }
    graphics::legend("topleft",
      legend = c("observed y", paste0("true tau=", taus), paste0("MCMC tau=", taus)),
      col = c("grey35", cols, cols),
      pch = c(16, rep(NA, 2L * length(taus))),
      lty = c(NA, rep(2, length(taus)), rep(1, length(taus))),
      lwd = c(NA, rep(2.0, length(taus)), rep(1.6, length(taus))),
      bty = "n", cex = 0.70
    )
  })
}

