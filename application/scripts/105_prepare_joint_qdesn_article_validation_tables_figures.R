make_heatmap <- function(path, data, value_col, title, legend_label) {
  dat <- data
  scen <- unique(dat$Scenario)
  mods <- unique(dat$Model)
  mat <- matrix(NA_real_, nrow = length(scen), ncol = length(mods), dimnames = list(scen, mods))
  for (ii in seq_len(nrow(dat))) mat[dat$Scenario[[ii]], dat$Model[[ii]]] <- dat[[value_col]][[ii]]
  pdf_plot(path, width = 7.8, height = 6.3, {
    graphics::par(mar = c(8.4, 9.2, 3.2, 4.6))
    z <- mat[nrow(mat):1, , drop = FALSE]
    pal <- grDevices::hcl.colors(80, "Blues 3", rev = FALSE)
    graphics::image(seq_len(ncol(z)), seq_len(nrow(z)), t(z), col = pal, axes = FALSE, xlab = "", ylab = "", main = title)
    graphics::axis(1, at = seq_len(ncol(z)), labels = colnames(z), las = 2, cex.axis = 0.76)
    graphics::axis(2, at = seq_len(nrow(z)), labels = rownames(z), las = 1, cex.axis = 0.80)
    for (i in seq_len(nrow(z))) {
      for (j in seq_len(ncol(z))) {
        graphics::text(j, i, fmt_num(z[i, j], 3), cex = 0.78, col = ifelse(z[i, j] > stats::quantile(z, 0.78, na.rm = TRUE), "white", "black"))
      }
    }
    graphics::box()
    graphics::mtext(legend_label, side = 4, line = 3.2, cex = 0.85)
  })
}

plot_truth_by_tau <- function(path, truth_distance, stable_ids) {
