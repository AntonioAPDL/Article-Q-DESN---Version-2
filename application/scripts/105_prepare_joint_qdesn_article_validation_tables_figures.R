plot_overlay <- function(path, forecast_truth, scenario_id) {
  dat <- forecast_truth[forecast_truth$scenario_id == scenario_id & forecast_truth$model_id == "joint_qdesn_rhs_vb", , drop = FALSE]
  keep_tau <- c(0.05, 0.50, 0.95)
  dat <- dat[dat$tau %in% keep_tau, , drop = FALSE]
  agg <- aggregate(cbind(y, true_quantile, qhat) ~ effective_index + tau, dat, mean, na.rm = TRUE)
  obs <- aggregate(y ~ effective_index, agg, mean, na.rm = TRUE)
  taus <- sort(unique(agg$tau))
  cols <- c("#0072B2", "#000000", "#D55E00")[seq_along(taus)]
  pdf_plot(path, width = 7.4, height = 4.8, {
    graphics::par(mar = c(4.5, 4.8, 3.0, 1.0))
    yr <- range(c(obs$y, agg$true_quantile, agg$qhat), finite = TRUE)
    graphics::plot(obs$effective_index, obs$y, pch = 16, cex = 0.35, col = grDevices::adjustcolor("grey35", 0.45),
      ylim = yr, xlab = "Effective time index", ylab = "Observed value and quantile", main = scenario_label(scenario_id))
    graphics::grid(col = "grey88")
    for (kk in seq_along(taus)) {
      block <- agg[abs(agg$tau - taus[[kk]]) < 1e-12, , drop = FALSE]
      block <- block[order(block$effective_index), , drop = FALSE]
      graphics::lines(block$effective_index, block$true_quantile, col = cols[[kk]], lwd = 2.1, lty = 2)
      graphics::lines(block$effective_index, block$qhat, col = cols[[kk]], lwd = 1.7)
    }
    graphics::legend("topleft",
      legend = c("observed y", paste0("true tau=", taus), paste0("fitted tau=", taus)),
      col = c("grey35", cols, cols), pch = c(16, rep(NA, 2 * length(taus))),
      lty = c(NA, rep(2, length(taus)), rep(1, length(taus))),
      lwd = c(NA, rep(2.1, length(taus)), rep(1.7, length(taus))),
      bty = "n", cex = 0.72
    )
  })
}

