write_latex_table <- function(x, path, caption, label, align = NULL, size = "\\small", resize = FALSE) {
  app_ensure_dir(dirname(path))
  if (is.null(align)) {
    align <- paste0("@{}", paste(ifelse(vapply(x, is.numeric, logical(1L)), "r", "l"), collapse = ""), "@{}")
  }
  body <- lapply(seq_len(nrow(x)), function(ii) {
    paste(latex_escape(unlist(x[ii, , drop = FALSE], use.names = FALSE)), collapse = " & ")
  })
  lines <- c(
    "% Generated by application/scripts/105_prepare_joint_qdesn_article_validation_tables_figures.R.",
    "\\begin{table}[!htbp]",
    "\\centering",
    size
  )
  if (isTRUE(resize)) {
    lines <- c(lines, "\\resizebox{\\textwidth}{!}{%")
  }
  lines <- c(
    lines,
    sprintf("\\begin{tabular}{%s}", align),
    "\\toprule",
    paste(latex_escape(names(x)), collapse = " & "),
    "\\\\",
    "\\midrule",
    paste0(body, " \\\\"),
    "\\bottomrule",
    "\\end{tabular}"
  )
  if (isTRUE(resize)) {
    lines <- c(lines, "}%")
  }
  lines <- c(
    lines,
    sprintf("\\caption{%s}", caption),
    sprintf("\\label{%s}", label),
    "\\end{table}"
  )
  writeLines(lines, path, useBytes = TRUE)
  normalizePath(path, winslash = "/", mustWork = TRUE)
}

