  scope <- match.arg(scope)
  out <- data.frame(
    Model = summary$model_label,
    Readout = app_joint_qdesn_article_readout_label(summary$fit_structure),
    `Fit MAE` = vapply(summary$fit_truth_mae, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Forecast MAE` = vapply(summary$forecast_truth_mae, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Forecast RMSE` = vapply(summary$forecast_truth_rmse, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Check loss` = vapply(summary$forecast_check_loss, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Grid CRPS` = vapply(summary$crps_grid_mean, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Hit-rate error` = vapply(summary$abs_hit_rate_error, app_joint_qdesn_article_fmt_num, character(1L), digits = 3),
    `Pre-rearrangement crossings` = vapply(summary$forecast_raw_crossings, app_joint_qdesn_article_fmt_int, character(1L)),
    stringsAsFactors = FALSE,
    check.names = FALSE
  )
  if (identical(scope, "main")) {
    out <- out[, c(
      "Model", "Fit MAE", "Forecast MAE", "Check loss", "Grid CRPS",
      "Hit-rate error", "Pre-rearrangement crossings"
    ), drop = FALSE]
    names(out)[names(out) == "Pre-rearrangement crossings"] <- "Raw crossings"
  }
  if (identical(scope, "provenance")) {
    out <- data.frame(
      Model = out$Model,
      Likelihood = app_joint_qdesn_article_likelihood_label(summary$likelihood),
      out[, setdiff(names(out), "Model"), drop = FALSE],
      stringsAsFactors = FALSE,
      check.names = FALSE
    )
    out$`Reported-grid crossings` <- vapply(summary$forecast_contract_crossings, app_joint_qdesn_article_fmt_int, character(1L))
    out$`Diagnostic status` <- summary$article_gate
  }
  out
}

app_joint_qdesn_article_make_vb_scenario_table <- function(src, bold_winners = FALSE) {
  fc <- src$phase107$forecast_scenario
  scenario_ids <- unique(fc$scenario_id)
  ids <- c("joint_qdesn_rhs_vb", "qdesn_rhs_independent_vb", "joint_exqdesn_rhs_vb", "exqdesn_rhs_independent_vb")
  rows <- lapply(scenario_ids, function(sc) {
    block <- fc[fc$scenario_id == sc & fc$model_id %in% ids, , drop = FALSE]
    vals <- setNames(rep("--", length(ids)), ids)
    min_mae_display <- app_joint_qdesn_article_fmt_num(min(block$truth_mae, na.rm = TRUE), digits = 3)
    for (id in ids) {
      row <- block[block$model_id == id, , drop = FALSE]
      if (nrow(row)) {
        val <- app_joint_qdesn_article_fmt_pair(row$truth_mae[[1L]], row$truth_rmse[[1L]])
        if (isTRUE(bold_winners) && identical(app_joint_qdesn_article_fmt_num(row$truth_mae[[1L]], digits = 3), min_mae_display)) {
          val <- sprintf("\\textbf{%s}", val)
        }
        vals[[id]] <- val
      }
    }
    data.frame(
      Scenario = app_joint_qdesn_article_scenario_label(sc),
      `Joint QDESN` = vals[["joint_qdesn_rhs_vb"]],
      `Independent QDESN` = vals[["qdesn_rhs_independent_vb"]],
      `Joint exQDESN` = vals[["joint_exqdesn_rhs_vb"]],
      `Independent exQDESN` = vals[["exqdesn_rhs_independent_vb"]],
      stringsAsFactors = FALSE,
      check.names = FALSE
    )
  })
  app_bind_rows_fill(rows)
}

