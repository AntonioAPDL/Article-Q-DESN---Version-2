  utils::write.csv(x, path, row.names = FALSE, na = "")
  normalizePath(path, winslash = "/", mustWork = TRUE)
}

app_joint_qdesn_article_write_latex_table <- function(
  x,
  path,
  caption,
  label,
  align = NULL,
  size = "\\small",
  resize = FALSE,
  raw_latex_cols = character()
) {
  app_ensure_dir(dirname(path))
  if (is.null(align)) {
    align <- paste0("@{}", paste(rep("l", ncol(x)), collapse = ""), "@{}")
  }
  body <- lapply(seq_len(nrow(x)), function(ii) {
    cells <- unlist(x[ii, , drop = FALSE], use.names = FALSE)
    is_raw <- names(x) %in% raw_latex_cols
    cells[!is_raw] <- app_joint_qdesn_article_latex_escape(cells[!is_raw])
    paste(cells, collapse = " & ")
  })
  lines <- c(
    "% Generated by application/scripts/110_build_joint_qdesn_article_validation_assets.R.",
    "\\begin{table}[!htbp]",
    "\\centering",
    size
  )
  if (isTRUE(resize)) lines <- c(lines, "\\resizebox{\\textwidth}{!}{%")
  lines <- c(
    lines,
    sprintf("\\begin{tabular}{%s}", align),
    "\\toprule",
    paste(app_joint_qdesn_article_latex_escape(names(x)), collapse = " & "),
    "\\\\",
    "\\midrule",
    paste0(body, " \\\\"),
    "\\bottomrule",
    "\\end{tabular}"
  )
  if (isTRUE(resize)) lines <- c(lines, "}%")
  lines <- c(
    lines,
    sprintf("\\caption{%s}", caption),
    sprintf("\\label{%s}", label),
    "\\end{table}"
  )
  writeLines(lines, path, useBytes = TRUE)
  normalizePath(path, winslash = "/", mustWork = TRUE)
}

