    scenario_csv = app_joint_qdesn_phase126_write_csv(src$case, file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_scenario_summary.csv")),
    scenario_tex = app_joint_qdesn_phase126_write_latex_table(
      scenario_latex_table,
      file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_scenario_summary.tex"),
      "Scenario-level balanced MCMC forecast recovery. Entries report forecast MAE with fit-window MAE in parentheses; boldface marks the lowest forecast MAE within each scenario. The table is a mechanism-specific diagnostic, not a uniform global ranking.",
      "tab:joint-qdesn-article-validation-mcmc-balanced-scenario-summary",
      align = "@{}>{\\raggedright\\arraybackslash}p{0.26\\textwidth}rrrr@{}",
      size = "\\scriptsize",
      resize = TRUE,
      raw_latex_cols = c("Joint QDESN", "Independent QDESN", "Joint exQDESN", "Independent exQDESN")
    ),
    gate_csv = app_joint_qdesn_phase126_write_csv(src$gate, file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_gate_summary.csv")),
    gate_tex = app_joint_qdesn_phase126_write_latex_table(
      gate_table,
      file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_gate_summary.tex"),
      "Validation gates for the Phase 125 balanced MCMC evidence packet used to build the article-facing joint QDESN tables.",
      "tab:joint-qdesn-article-validation-mcmc-balanced-gate-summary",
      align = "@{}>{\\raggedright\\arraybackslash}p{0.24\\textwidth}l>{\\raggedright\\arraybackslash}p{0.58\\textwidth}@{}",
      size = "\\scriptsize",
      resize = TRUE
    ),
    winner_csv = app_joint_qdesn_phase126_write_csv(src$winners, file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_winner_summary.csv")),
    winner_tex = app_joint_qdesn_phase126_write_latex_table(
      winner_table,
      file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_winner_summary.tex"),
      "Per-scenario metric winners in the balanced MCMC confirmation grid. The winner can differ by metric, so this table is intended for diagnostic interpretation rather than a single-model ranking.",
      "tab:joint-qdesn-article-validation-mcmc-balanced-winner-summary",
      align = "@{}>{\\raggedright\\arraybackslash}p{0.21\\textwidth}>{\\raggedright\\arraybackslash}p{0.17\\textwidth}>{\\raggedright\\arraybackslash}p{0.17\\textwidth}>{\\raggedright\\arraybackslash}p{0.17\\textwidth}>{\\raggedright\\arraybackslash}p{0.17\\textwidth}@{}",
      size = "\\scriptsize",
      resize = TRUE
    )
  )

  table_paths[["main_wrapper"]] <- app_joint_qdesn_phase126_write_wrapper(
    table_paths[c("model_tex")],
    file.path(tables_dir, "joint_qdesn_article_validation_tables.tex"),
    "Compact balanced MCMC article table."
  )
  table_paths[["provenance_wrapper"]] <- app_joint_qdesn_phase126_write_wrapper(
    table_paths[c("protocol_tex", "scenario_tex", "gate_tex", "winner_tex")],
    file.path(tables_dir, "joint_qdesn_article_validation_provenance_tables.tex"),
    "Balanced MCMC protocol and provenance tables."
  )

  asset_paths <- table_paths
  asset_manifest <- data.frame(
    label = names(asset_paths),
    artifact_type = ifelse(grepl("\\.tex$", asset_paths), "latex_table", "csv_table"),
    path = vapply(asset_paths, app_prefer_repo_relative_path, character(1L)),
    size_bytes = as.numeric(file.info(asset_paths)$size),
    sha256 = vapply(asset_paths, app_sha256_file, character(1L)),
    source_phase125_dir = app_prefer_repo_relative_path(phase125_dir),
    source_validation_contract = "quantile_grid_readout_fit_and_no_refit_forecast",
    stringsAsFactors = FALSE
  )
  table_paths[["asset_manifest"]] <- app_joint_qdesn_phase126_write_csv(
    asset_manifest,
    file.path(tables_dir, "joint_qdesn_article_validation_asset_manifest.csv")
  )

  readiness <- app_joint_qdesn_phase126_readiness(src, asset_manifest)
  run_config <- data.frame(
    run_id = "joint_qdesn_phase126_article_assets",
    phase125_dir = phase125_dir,
    tables_dir = tables_dir,
    output_dir = out_dir,
    n_article_assets = nrow(asset_manifest),
    n_scenarios = readiness$n_scenarios[[1L]],
    n_case_rows = readiness$n_case_rows[[1L]],
    n_model_rows = readiness$n_model_rows[[1L]],
    hard_implementation_gate = readiness$hard_implementation_gate[[1L]],
    overall_gate = readiness$overall_gate[[1L]],
    validation_contract = "quantile_grid_readout_fit_and_no_refit_forecast",
    scalar_predictive_density_claim = FALSE,
    created_at = format(Sys.time(), "%Y-%m-%d %H:%M:%S %Z"),
    stringsAsFactors = FALSE
  )

  readme_path <- file.path(out_dir, "README.md")
  writeLines(app_joint_qdesn_phase126_readme(run_config, readiness), readme_path, useBytes = TRUE)

  cache_paths <- c(
    run_config = app_joint_qdesn_phase126_write_csv(run_config, file.path(out_dir, "run_config.csv")),
    phase125_artifact_manifest_verification = app_joint_qdesn_phase126_write_csv(src$artifact_manifest_verification, file.path(out_dir, "phase125_artifact_manifest_verification.csv")),
    source_artifact_manifest_verification = app_joint_qdesn_phase126_write_csv(src$source_artifact_manifest_verification, file.path(out_dir, "source_artifact_manifest_verification.csv")),
    source_vb_freeze_manifest_verification = app_joint_qdesn_phase126_write_csv(src$source_vb_freeze_manifest_verification, file.path(out_dir, "source_vb_freeze_manifest_verification.csv")),
    fixture_source_manifest_verification = app_joint_qdesn_phase126_write_csv(src$fixture_source_manifest_verification, file.path(out_dir, "fixture_source_manifest_verification.csv")),
    model_article_table = app_joint_qdesn_phase126_write_csv(model_table, file.path(out_dir, "mcmc_balanced_model_article_table.csv")),
    scenario_article_table = app_joint_qdesn_phase126_write_csv(scenario_table, file.path(out_dir, "mcmc_balanced_scenario_article_table.csv")),
    winner_article_table = app_joint_qdesn_phase126_write_csv(winner_table, file.path(out_dir, "mcmc_balanced_winner_article_table.csv")),
    gate_article_table = app_joint_qdesn_phase126_write_csv(gate_table, file.path(out_dir, "mcmc_balanced_gate_article_table.csv")),
    protocol_table = app_joint_qdesn_phase126_write_csv(protocol, file.path(out_dir, "mcmc_balanced_protocol_table.csv")),
    article_asset_manifest = app_joint_qdesn_phase126_write_csv(asset_manifest, file.path(out_dir, "article_asset_manifest.csv")),
    readiness = app_joint_qdesn_phase126_write_csv(readiness, file.path(out_dir, "article_asset_readiness.csv")),
    provenance = app_joint_qdesn_phase126_write_csv(app_joint_qvp_provenance_rows(), file.path(out_dir, "provenance.csv")),
    readme = normalizePath(readme_path, winslash = "/", mustWork = TRUE)
  )
  manifest_info <- app_joint_qdesn_write_manifest(cache_paths, out_dir)

  list(
    out_dir = normalizePath(out_dir, winslash = "/", mustWork = TRUE),
    tables_dir = tables_dir,
    paths = c(cache_paths, article_artifacts = table_paths, artifact_manifest = manifest_info$manifest_path),
    phase125 = src,
    run_config = run_config,
    readiness = readiness,
    asset_manifest = asset_manifest,
    artifact_manifest = manifest_info$manifest,
    artifact_manifest_path = manifest_info$manifest_path,
    model_table = model_table,
    scenario_table = scenario_table,
    gate_table = gate_table,
    winner_table = winner_table
  )
}

app_joint_qdesn_phase126_text_contains <- function(path, pattern, fixed = TRUE) {
  if (!file.exists(path)) return(FALSE)
  txt <- paste(readLines(path, warn = FALSE), collapse = "\n")
  grepl(pattern, txt, fixed = fixed)
}

app_joint_qdesn_run_phase126_article_asset_audit <- function(
  phase126_dir = app_joint_qdesn_default_phase126_article_assets_dir(),
  tables_dir = app_path("tables"),
  main_tex = app_path("main.tex"),
  supplement_tex = app_path("qdesn-supplement.tex"),
  out_dir = file.path(phase126_dir, "phase126_readiness_audit")
) {
  phase126_dir <- normalizePath(phase126_dir, winslash = "/", mustWork = TRUE)
  tables_dir <- normalizePath(tables_dir, winslash = "/", mustWork = TRUE)
  main_tex <- normalizePath(main_tex, winslash = "/", mustWork = TRUE)
  supplement_tex <- normalizePath(supplement_tex, winslash = "/", mustWork = TRUE)
  out_dir <- normalizePath(out_dir, winslash = "/", mustWork = FALSE)
  app_ensure_dir(out_dir)

  artifact_verification <- app_joint_qdesn_phase126_verify_manifest(phase126_dir, "artifact_manifest.csv", "phase126_artifact")
  article_asset_manifest_path <- file.path(phase126_dir, "article_asset_manifest.csv")
  article_asset_verification <- app_joint_qdesn_phase126_verify_article_asset_manifest(article_asset_manifest_path)
  readiness <- app_read_csv(file.path(phase126_dir, "article_asset_readiness.csv"))
  asset_manifest <- app_read_csv(article_asset_manifest_path)

  manuscript_checks <- data.frame(
    check = c(
      "main_uses_balanced_mcmc_table",
      "main_does_not_use_stale_primary_mcmc_table",
      "main_mentions_eight_scenarios",
      "main_mentions_32_rows",
      "main_mentions_quantile_grid_contract",
      "main_avoids_scalar_density_claim",
      "main_avoids_nine_mechanism_claim",
      "main_avoids_zero_raw_crossing_claim",
      "supplement_uses_balanced_provenance_wrapper"
    ),
    status = c(
      ifelse(app_joint_qdesn_phase126_text_contains(main_tex, "joint_qdesn_article_validation_mcmc_balanced_model_summary.tex"), "pass", "fail"),
      ifelse(!app_joint_qdesn_phase126_text_contains(main_tex, "joint_qdesn_article_validation_mcmc_main_summary.tex"), "pass", "fail"),
      ifelse(app_joint_qdesn_phase126_text_contains(main_tex, "eight synthetic") || app_joint_qdesn_phase126_text_contains(main_tex, "eight-scenario"), "pass", "fail"),
      ifelse(app_joint_qdesn_phase126_text_contains(main_tex, "32") && app_joint_qdesn_phase126_text_contains(main_tex, "scenario--model"), "pass", "fail"),
      ifelse(app_joint_qdesn_phase126_text_contains(main_tex, "quantile-grid") || app_joint_qdesn_phase126_text_contains(main_tex, "quantile grid"), "pass", "fail"),
      ifelse(!app_joint_qdesn_phase126_text_contains(main_tex, "scalar posterior predictive density validation"), "pass", "fail"),
      ifelse(!app_joint_qdesn_phase126_text_contains(main_tex, "nine synthetic data-generating mechanisms") && !app_joint_qdesn_phase126_text_contains(main_tex, "all nine mechanisms"), "pass", "fail"),
      ifelse(!app_joint_qdesn_phase126_text_contains(main_tex, "raw adjacent-level crossing count is zero"), "pass", "fail"),
      ifelse(app_joint_qdesn_phase126_text_contains(supplement_tex, "joint_qdesn_article_validation_provenance_tables.tex"), "pass", "fail")
    ),
    detail = c(
      "Main text should input the Phase126 balanced MCMC model table.",
      "Main text should not input the older primary-row MCMC table.",
      "Main text should describe the Phase125/126 eight-scenario balanced grid.",
      "Main text should state the 32 scenario-model rows.",
      "Main text should keep claims scoped to the quantile-grid predictive contract.",
      "Main text should not claim scalar posterior predictive density validation.",
      "Main text should not retain old nine-mechanism language.",
      "Main text should not claim all raw crossings are zero in the balanced grid.",
      "Supplement should include the balanced protocol/provenance wrapper."
    ),
    stringsAsFactors = FALSE
  )

  asset_checks <- data.frame(
    check = c(
      "phase126_manifest_hashes",
      "article_asset_manifest_hashes",
      "article_assets_source_phase125",
      "balanced_model_table_present",
      "balanced_model_table_in_manifest",
      "hard_implementation_gate",
      "contract_crossings_zero",
      "raw_crossings_reviewed"
    ),
    status = c(
      ifelse(all(artifact_verification$status == "pass"), "pass", "fail"),
      ifelse(all(article_asset_verification$status == "pass"), "pass", "fail"),
      ifelse(all(grepl("phase125", asset_manifest$source_phase125_dir)), "pass", "fail"),
      ifelse(file.exists(file.path(tables_dir, "joint_qdesn_article_validation_mcmc_balanced_model_summary.tex")), "pass", "fail"),
      ifelse(any(asset_manifest$label == "model_tex"), "pass", "fail"),
      ifelse(identical(as.character(readiness$hard_implementation_gate[[1L]]), "pass"), "pass", "fail"),
      ifelse(as.numeric(readiness$mcmc_forecast_contract_crossing_pairs[[1L]]) == 0, "pass", "fail"),
      ifelse(as.numeric(readiness$mcmc_forecast_raw_crossing_pairs[[1L]]) > 0, "review", "pass")
    ),
    detail = c(
      sprintf("%d/%d Phase126 manifest rows pass.", sum(artifact_verification$status == "pass"), nrow(artifact_verification)),
      sprintf("%d/%d article asset rows pass.", sum(article_asset_verification$status == "pass"), nrow(article_asset_verification)),
      "Generated article asset manifest records Phase125 as the source evidence packet.",
      "Balanced model table exists in the requested tables directory.",
      "Balanced model table is present in the Phase126 article asset manifest.",
      sprintf("hard_implementation_gate=%s", readiness$hard_implementation_gate[[1L]]),
      sprintf("mcmc_forecast_contract_crossing_pairs=%s", readiness$mcmc_forecast_contract_crossing_pairs[[1L]]),
      sprintf("mcmc_forecast_raw_crossing_pairs=%s", readiness$mcmc_forecast_raw_crossing_pairs[[1L]])
    ),
    stringsAsFactors = FALSE
  )

  all_status <- c(manuscript_checks$status, asset_checks$status)
  audit_status <- if (any(all_status == "fail")) "fail" else if (any(all_status == "review")) "review" else "pass"
  audit_summary <- data.frame(
    audit_status = audit_status,
    phase126_dir = phase126_dir,
    n_asset_checks = nrow(asset_checks),
    n_manuscript_checks = nrow(manuscript_checks),
    n_fail = sum(all_status == "fail"),
    n_review = sum(all_status == "review"),
    n_pass = sum(all_status == "pass"),
    next_action = if (identical(audit_status, "fail")) {
      "Fix failed asset or manuscript checks before using Phase126 in the article."
    } else {
      "Use Phase126 assets with explicit review language for raw pre-contract crossings."
    },
    stringsAsFactors = FALSE
  )

  readme_path <- file.path(out_dir, "README.md")
  writeLines(c(
    "# Joint QDESN Phase 126 Article-Asset Audit",
    "",
    sprintf("- Audit status: `%s`", audit_status),
    sprintf("- Phase126 directory: `%s`", phase126_dir),
    sprintf("- Failed checks: `%s`", audit_summary$n_fail[[1L]]),
    sprintf("- Review checks: `%s`", audit_summary$n_review[[1L]]),
    "",
    "This audit verifies hashes, source provenance, balanced-table presence, and manuscript wording against the Phase125 balanced MCMC evidence packet."
  ), readme_path, useBytes = TRUE)

  paths <- c(
    audit_summary = app_joint_qdesn_phase126_write_csv(audit_summary, file.path(out_dir, "phase126_article_asset_audit_summary.csv")),
    asset_checks = app_joint_qdesn_phase126_write_csv(asset_checks, file.path(out_dir, "phase126_asset_checks.csv")),
    manuscript_checks = app_joint_qdesn_phase126_write_csv(manuscript_checks, file.path(out_dir, "phase126_manuscript_checks.csv")),
    artifact_manifest_verification = app_joint_qdesn_phase126_write_csv(artifact_verification, file.path(out_dir, "phase126_artifact_manifest_verification.csv")),
    article_asset_manifest_verification = app_joint_qdesn_phase126_write_csv(article_asset_verification, file.path(out_dir, "phase126_article_asset_manifest_verification.csv")),
    readiness = app_joint_qdesn_phase126_write_csv(readiness, file.path(out_dir, "phase126_article_asset_readiness.csv")),
    provenance = app_joint_qdesn_phase126_write_csv(app_joint_qvp_provenance_rows(), file.path(out_dir, "provenance.csv")),
    readme = normalizePath(readme_path, winslash = "/", mustWork = TRUE)
  )
  manifest_info <- app_joint_qdesn_write_manifest(paths, out_dir)

  list(
    out_dir = normalizePath(out_dir, winslash = "/", mustWork = TRUE),
    audit_summary = audit_summary,
    asset_checks = asset_checks,
    manuscript_checks = manuscript_checks,
    artifact_manifest_verification = artifact_verification,
    article_asset_manifest_verification = article_asset_verification,
    paths = c(paths, artifact_manifest = manifest_info$manifest_path),
    artifact_manifest = manifest_info$manifest
  )
}
