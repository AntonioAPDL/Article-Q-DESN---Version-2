model_ids_main <- function(include_independent_exal) {
  ids <- c("joint_qdesn_rhs_vb", "qdesn_rhs_independent_vb", "joint_exqdesn_rhs_vb")
  if (isTRUE(include_independent_exal)) ids <- c(ids, "exqdesn_rhs_independent_vb")
  ids
}

make_model_summary <- function(model_metric, fit_assessment, forecast_assessment, include_independent_exal) {
  ids <- model_ids_main(include_independent_exal)
  fit_m <- model_metric[model_metric$stage == "fit" & model_metric$model_id %in% ids, , drop = FALSE]
  forecast_m <- model_metric[model_metric$stage == "forecast" & model_metric$model_id %in% ids, , drop = FALSE]
  by_model <- c("model_id", "display_label", "likelihood", "fit_structure")
  fit_sel <- fit_m[, c(by_model, "truth_mae", "truth_rmse", "check_loss_mean"), drop = FALSE]
  names(fit_sel)[names(fit_sel) %in% c("truth_mae", "truth_rmse", "check_loss_mean")] <- c("fit_truth_mae", "fit_truth_rmse", "fit_check_loss")
  fc_sel <- forecast_m[, c(by_model, "truth_mae", "truth_rmse", "check_loss_mean", "crps_grid_mean", "abs_hit_rate_error", "abs_coverage_error"), drop = FALSE]
  names(fc_sel)[names(fc_sel) %in% c("truth_mae", "truth_rmse", "check_loss_mean")] <- c("forecast_truth_mae", "forecast_truth_rmse", "forecast_check_loss")
  fta <- fit_assessment[fit_assessment$model_id %in% ids, , drop = FALSE]
  fca <- forecast_assessment[forecast_assessment$model_id %in% ids, , drop = FALSE]
  fit_a <- aggregate(cbind(raw_crossing_pairs, contract_crossing_pairs, reached_max_iter, elapsed_seconds) ~ model_id, fta, sum, na.rm = TRUE)
  names(fit_a)[-1L] <- paste0("fit_", names(fit_a)[-1L])
  forecast_a <- aggregate(cbind(raw_crossing_pairs, contract_crossing_pairs, reached_max_iter, elapsed_seconds) ~ model_id, fca, sum, na.rm = TRUE)
  names(forecast_a)[-1L] <- paste0("forecast_", names(forecast_a)[-1L])
  max_adj <- aggregate(max_abs_adjustment ~ model_id, fca, max, na.rm = TRUE)
  names(max_adj)[2L] <- "forecast_max_abs_adjustment"
  out <- merge(fit_sel, fc_sel, by = by_model, all = TRUE)
  out <- Reduce(function(a, b) merge(a, b, by = "model_id", all = TRUE), list(out, fit_a, forecast_a, max_adj))
  out$article_label <- article_model_label(out$display_label)
  out$article_role <- ifelse(out$model_id == "joint_qdesn_rhs_vb", "primary joint model",
    ifelse(out$model_id == "qdesn_rhs_independent_vb", "independent AL comparator",
      ifelse(out$model_id == "joint_exqdesn_rhs_vb", "joint exAL comparator", "diagnostic only")))
  out$main_table_included <- out$model_id %in% ids & out$model_id != "exqdesn_rhs_independent_vb"
  out[match(ids, out$model_id), , drop = FALSE]
}

